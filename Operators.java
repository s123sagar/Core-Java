package com.company;

public class Operators {
    public static void main(String[] args) {
        int sum1 = 100 + 50;
        int sum2 = sum1 + 250;                  // +	Addition
        int sum3 = sum2 + sum2;

        int x = 5;
        ++x;                                    // ++	Increment
        System.out.println(x);

        int Y = 5;
        --Y;                                    // --	Decrement
        System.out.println(Y);

        System.out.println(sum1);
        System.out.println(sum2);
        System.out.println(sum3);
                                                // -	Subtraction
                                                // *    Multiplication
                                                //  /	Division
                                                //  %	Modulus


    }
}


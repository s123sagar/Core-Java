package com.company;

public class IncrementDecrementOperators3 {

	public static void main(String[] args) {
		
		 //Pre-Increment Operator
	       
	      int A = 5;
	      int B = 7;
	       
	      int C = ++A + B; // Here 'a' will be incremented immediately,
	                       // and ++a will return value 6.
	                       // C =  6  + 7   and this will evaluate to 13.
	       
	      System.out.println("Pre- Increment \n C = "+ C);
	}

}

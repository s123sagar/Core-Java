package com.company;

public class ArithematicOperators {

	public static void main(String[] args) {
		
		int a = 10;
		char b = 'a';                   //ASCII Value of a is 97 
		
		int c = 24;
		String d = "sagar";
		
		System.out.println(a+b);      //output = 97+10
		                             //        =107 (char + integer)
		
		
		System.out.println(c+d);      //output = 24sagar (integer + String)
		}

}

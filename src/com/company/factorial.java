package com.company;

import java.util.Scanner;

public class factorial {

		public static void main(String[] args) {
          
	        int n=5,fact=1;

	        for(int i=1;i<=n;i++)
	  	    {
	    	   fact=fact*i;
		 	  }

		        System.out.println("factoral="+fact);
	}
	}
//fact = n(n!)


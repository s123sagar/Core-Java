package com.company;

// Java while loop is used to run a specific code until a certain condition is met. 
// Entry control loops

public class All_10_EvenNumber {

	public static void main(String[] args) {
		
		// declare variables
		int a=1;
		
		while(a<=10) {	
		  if(a%2==0) {
			System.out.println(a);
		}
            a++;
	}
	}
}

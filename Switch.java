package com.company;

public class Switch {
    public static void main(String[]args){
       // int day = 4;
        int day = 7;
        switch (day){                                             // Switch Statement
            case 1:
            System.out.println("Monday");
            break;
            case 2:
            System.out.println("Tuseday");
            break;
            case 3:
            System.out.println("Wenasday");
            break;
            case 4:
            System.out.println("Thursday");
            break;
            case 5:
            System.out.println("Friday");
            break;
            case 6:
            System.out.println("stauraday");
            break;
            default:                                               // default Statement
                System.out.println("Its no day");
        }
    }

}

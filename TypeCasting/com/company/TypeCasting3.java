package com.company;

public class TypeCasting3 {

	public static void main(String[] args) {
		
        float a = 12;
       // float a = 1.2;  Not convert Double to float
        float b = 15;
        
        int c = (int)a;
        int d = (int)b;
        
        System.out.println(c);
        System.out.println(d);
	}

}
